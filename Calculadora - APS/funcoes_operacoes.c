#include "funcoes_operacoes.h"

int soma(int a, int b){
    int resultado;
    resultado = a + b;
    return resultado;
}

int subtracao(int a, int b){
    int resultado;
    resultado = a - b;
    return resultado;
}

int multiplicacao(int a, int b){
    int resultado;
    resultado = a * b;
    return resultado;
}

int produto_escalar(int a, int b){
    int resultado;
    resultado += a * b;
    return resultado;
}
